"""
№112392. Минимум и максимум в файле
Найти минимальное и максимальное из чётных положительных чисел, записанных в файле input.txt .
Количество чисел неизвестно. Если в файле нет чётных положительных чисел, нужно вывести число 0. 
Результат записать в файл output.txt .

Входные данные
Целые числа записаны в файле input.txt в столбик, по одному в строке. 
Ввод заканчивается тогда, когда заканчиваются данные в файле.

Выходные данные
Программа должна вывести в файл output.txt минимальное и максимальное из чётных положительных чисел,
разделив их пробелом. 
Если в файле нет чётных положительных чисел, нужно вывести число 0.

Примеры
Входные данные (файл input.txt):
1
2
3
4
-5
-8
Выходные данные (файл output.txt):
2 4
"""

even_positives = []  # список для хранения четных положительных чисел

with open("input_even_minmax.txt", "r") as infile:  # открытие файла для чтения
    for line in infile:  # чтение файла построчно
        num = int(line.strip())  # преобразование строки в целое число
        if num > 0 and num % 2 == 0:  # проверка число > 0 и четное
            even_positives.append(num)  # добавление в список

# Определение результата
if even_positives:  # Если список не пуст
    min_val = min(even_positives)  # Поиск минимального значения
    max_val = max(even_positives)  # Поиск максимального значения
    result = f"{min_val} {max_val}"  # Результат в виде "min max"
else:
    result = "0"

with open("output_even_minmax.txt", "w") as outfile:  # открытие файла для записи
    outfile.write(result)
